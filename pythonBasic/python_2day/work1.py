"""
集合自动去重
    创建集合：
        1.变量名 = set(元素) 字符串/列表/元组/字典
        2.变量名 = {元素，元素。。。}
        3.元素是唯一的
"""
# 字符串
a = set("1234") # {'3', '4', '1', '2'}
# 列表
b = set([10,20,30]) # {10, 20, 30}
print(b)
# 元组
c = set((1,2,3,4)) # {1, 2, 3, 4}
print(c)
# 字典
d = {
    "年龄": 18,
    "名字": "sqh"
}
f = set(d) # {'名字', '年龄'}
print(f)
# 用大括号创建集合
aa = {1,2,3,4,4,5,(1,2,3),(1,2,3)} # {1, 2, 3, 4, 5, (1, 2, 3)}
print(aa)
# 字符串去重
abc = set("123444444") # {'3', '2', '4', '1'}
print(abc)
