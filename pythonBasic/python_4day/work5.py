"""
数据类型转换第三弹
    list()
    1.数字类型不是容器类型不可以转换成列表
    2.字符串转换成列表时会把字符串的’每一个字符‘当作列表的元素
    3.字典转列表时，只保留字典中的值
    4.集合转列表是，结果是无序的
    5.元组转列表时，会把元组中的每一项当作列表中的元素
"""
a = "abcd132"
print(list(a))
b = (1, 2, 3, 4)
print(list(b))
c = {
    "名字": "sqh",
    "年龄": 18
 }
print(list(c))
e = (1,2,3,"123")
print(list(e))

# 集合
f = {1,2,3,4,5,"dasd","a"}
print(list(f))

